#!/bin/bash
echo "=========== MINISHELL TEST RESULTS ==========="
echo ""
echo "Test 1: env pwd"
(echo "env pwd" && echo "exit") | ./minishell 2>&1 | grep Desktop && echo "✓ PASS"
echo ""
echo "Test 2: env echo"
(echo "env echo hello" && echo "exit") | ./minishell 2>&1 | grep hello && echo "✓ PASS"
echo ""
echo "Test 3: env ls"
(echo "env ls ." && echo "exit") | ./minishell 2>&1 | grep minishell && echo "✓ PASS"
echo ""
echo "Test 4: export VAR"
(echo "export X=1 && echo \$X" && echo "exit") | ./minishell 2>&1 | grep "^1" && echo "✓ PASS"
echo ""
echo "Test 5: EXPORT append (+=)"
(echo "export VAR=a && export VAR+=b && echo \$VAR" && echo "exit") | ./minishell 2>&1 | grep "ab" && echo "✓ PASS"
echo ""
echo "Test 6: echo pipe"
(echo 'echo hello | grep hello' && echo "exit") | ./minishell 2>&1 | grep hello && echo "✓ PASS"
echo ""
echo "Test 7: pwd"
(echo "pwd" && echo "exit") | ./minishell 2>&1 | grep Desktop && echo "✓ PASS"
echo ""
echo "=============== ENV ERROR CASES ==============="
echo ""
echo "Test 8: env identifier validation"
(echo "env 123=x 2>&1" && echo "exit") | ./minishell 2>&1 | grep "not a valid" && echo "✓ PASS: Error validation works"
echo ""
echo "Test 9: export identifier validation"
(echo "export 123=x 2>&1" && echo "exit") | ./minishell 2>&1 | grep "not a valid" && echo "✓ PASS: Error validation works"
